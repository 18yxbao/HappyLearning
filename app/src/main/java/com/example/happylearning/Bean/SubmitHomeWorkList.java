package com.example.happylearning.Bean;

public class SubmitHomeWorkList {

    private String file_name;
    private String submitter_number;
    private String submitter_name;
    private String submit_time;
    private String class_number;
    private String file_path;


    public String getFile_name() {
        return file_name;
    }

    public void setFile_name(String file_name) {
        this.file_name = file_name;
    }

    public String getSubmitter_number() {
        return submitter_number;
    }

    public void setSubmitter_number(String submitter_number) {
        this.submitter_number = submitter_number;
    }

    public String getSubmitter_name() {
        return submitter_name;
    }

    public void setSubmitter_name(String submitter_name) {
        this.submitter_name = submitter_name;
    }

    public String getSubmit_time() {
        return submit_time;
    }

    public void setSubmit_time(String submit_time) {
        this.submit_time = submit_time;
    }

    public String getClass_number() {
        return class_number;
    }

    public void setClass_number(String class_number) {
        this.class_number = class_number;
    }

    public String getFile_path() {
        return file_path;
    }

    public void setFile_path(String file_path) {
        this.file_path = file_path;
    }


}
